SELECT
  category,
  city,
  country,
  customer_id,
  customer_name,
  discount,
  market,
  unknown,
  order_date,
  order_id,
  order_priority,
  product_id,
  product_name,
  profit,
  quantity,
  region,
  sales,
  segment,
  ship_date,
  ship_mode,
  shipping_cost,
  state,
  sub_category,
  year ,
  market2,
  weeknum, 
  COUNT(*) AS num_duplicates
FROM
  `my-project-laboratoria.project_rota01.superstore`
GROUP BY
  category,
  city,
  country,
  customer_id,
  customer_name,
  discount,
  market,
  unknown,
  order_date,
  order_id,
  order_priority,
  product_id,
  product_name,
  profit,
  quantity,
  region,
  sales,
  segment,
  ship_date,
  ship_mode,
  shipping_cost,
  state,
  sub_category,
  year ,
  market2,
  weeknum
HAVING
  COUNT(*) > 1